    public function Patient($id)
    {
        return \App\Appointment::where('patient_id', $id)->get();
    }

    public function Doctor($id)
    {
        return \App\Appointment::where('doctor_id', $id)->get();
    }

    public function Hospital($id)
    {
        return \App\Appointment::where('hospital_id', $id)->get();
    }
}
